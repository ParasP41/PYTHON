# 7 // 3 results in 2 because 7 / 3 is approximately 2.33, and the floor of 2.33 is 2.
# 7.0 // 3 results in 2.0, which is a float.
# -7 // 3 results in -3 because the floor of -2.33 is -3.
# -7.0 // 3 also results in -3.0.